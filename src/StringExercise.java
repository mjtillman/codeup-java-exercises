public class StringExercise {
  public static void main(String[] args) {
    String pinkFloyd = "We don't need no education\n" +
        "We don't need no thought control";

    String ess = "Check \"this\" out!, \"s inside of \"s!";

    String windows = "In windows, the main drive is usually C:\\";

    String tricks = "I can do backslashes \\, double backslashes \\\\,\n" +
        "and the amazing triple backslash \\\\\\!";

    System.out.println("\n" + pinkFloyd);
    System.out.println("\n" + ess);
    System.out.println("\n" + windows);
    System.out.println("\n" + tricks + "\n");
  }
}
